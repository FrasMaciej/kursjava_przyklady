public class SprawdzMianownik {
  public static void main(String[] args) {
    double licznik, mianownik;

    licznik = 5;
    mianownik = 0;

    if (mianownik != 0) {
      System.out.println("Wynik: " + licznik / mianownik);
    } else {
      System.out.println("Mianownik nie moze byc = 0!");
    }
  }
}